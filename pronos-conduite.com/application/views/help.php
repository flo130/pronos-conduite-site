<div class="row well">	
	<div class="panel panel-info">
		<div class="panel-heading">
			<h3 class="panel-title">Pourquoi ce site ?</h3>
		</div>
		Ce site a été développé pour pouvoir facilement gérer les pronostiques des utilisateurs sur les matchs de Guingamp. 
		<br />
		Il peut aussi servir pour enregistrer les tajets pour se rendre au stade.
	</div>

	<div class="panel panel-info">
		<div class="panel-heading">
			<h3 class="panel-title">Pourquoi s'incrire ?</h3>
		</div>
		Il faut s'inscrire pour pouvoir enregistrer un pronostique ou un trajet.
	</div>

	<div class="panel panel-info">
		<div class="panel-heading">
			<h3 class="panel-title">Comment s'incrire ?</h3>
		</div>
		<ul>
			<li>Cliquer sur le boutton "<a href="/user/register">S'incrire</a>" en haut à droite du site puis remplir les champs du formulaire.</li>
			<li>L'email sera utilisé pour effectuer des relances avant les matchs. Eventuellement pour faire passer des informations importantes.</li>
			<li>Le pseudo et le mot de passe servent pour la connexion.</li>
			<li>Une fois l'inscription faite, il faut se connecter en cliquant sur "<a href="/user/login">Se connecter</a>" en haut à droite du site.</li>
		</ul>
	</div>

	<div class="panel panel-info">
		<div class="panel-heading">
			<h3 class="panel-title">Comment se connecter ?</h3>
		</div>
		<ul>
			<li>Cliquer sur "<a href="/user/login">Se connecter</a>" en haut à droite du site.</li>
			<li>Saisir le login puis le mot de passe et valider.</li>
		</ul>
	</div>
	
	<div class="panel panel-info">
		<div class="panel-heading">
			<h3 class="panel-title">Comment parier ?</h3>
		</div>
		<ul>
			<li>Avant toute chose il faut être connecté sur le site (en cliquant sur "<a href="/user/login">Se connecter</a>" en haut à droite) pour pouvoir déposer un pronostique sur un match.</li>
			<li>Cliquer sur "Pronostiques" en haut du site puis sur "<a href="">Ajouter un pronostique</a>".</li>
			<li>Saisir le score du match puis valider. Le pronostique est alors visible dans la partie "<a href="/prognostic/stat">Statistiques</a>".</li>
		</ul>
	</div>
	
	<div class="panel panel-info">
		<div class="panel-heading">
			<h3 class="panel-title">Comment déclarer un trajet ?</h3>
		</div>
		<ul>
			<li>Avant toute chose il faut être connecté sur le site (en cliquant sur "<a href="/user/login">Se connecter</a>" en haut à droite) pour pouvoir déposer un trajet.</li>
			<li>Cliquer sur "Trajets" en haut du site puis sur "<a href="">Ajouter un trajet</a>".</li>
			<li>Saisir le match du trajet puis valider. Le trajet est alors visible dans la partie "<a href="/ride/stat">Qui a conduit ?</a>".</li>
		</ul>
	</div>
	
	<div class="panel panel-info">
		<div class="panel-heading">
			<h3 class="panel-title">D'ou viennent ces données ?</h3>
		</div>
		Toutes les données de type "calendrier" ou "classement" viennent de l'application mobile "match en direct" dont le site est le suivant : <a href="http://www.matchendirect.fr">www.matchendirect.fr</a>. Elle sont actualiées à chaque rafraichissement de page.
		<br />
		Les autres données sont entrées par utilisateurs du site.
	</div>
</div>	

